        public static string[] GetExtension(string mimeType)
        {
            if (mimeType == null)
            {
                return null;
            }
            mimeType = mimeType.ToLower();

            if (_mappings.ContainsKey(mimeType))
                return _mappings[mimeType];
            return null;
        }

        public static bool ExistExtensionOnDB(string extension)
        {
            foreach (var item in _mappings)
            {
                if (item.Value.Contains(extension))
                    return true;
            }
            return false;
        }
